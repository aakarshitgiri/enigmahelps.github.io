    <h4>Steps to help prevent the spread of COVID-19 if you are sick</h4>
<p>If you are sick with COVID-19 or think you might have COVID-19, follow the steps below to care for yourself and to help protect other people in your home and community.</p>
<h4>Stay home except to get medical care</h4>
<ul>
<li><p>Stay home. Most people with COVID-19 have mild illness and can recover at home without medical care. Do not leave your home, except to get medical care. Do not visit public areas.</li></p>
<li><p>Take care of yourself. Get rest and stay hydrated. Take over-the-counter medicines, such as acetaminophen, to help you feel better.</li></p>
<li><p>Stay in touch with your doctor. Call before you get medical care. Be sure to get care if you have trouble breathing, or have any other emergency warning signs, or if you think it is an emergency.</li></p>
<li><p>Avoid public transportation, ride-sharing, or taxis.</li></p>

    </ul>

    <h4>Separate yourself from other people</h4>
<p>As much as possible, stay in a specific room and away from other people and pets in your home. If possible, you should use a separate bathroom. If you need to be around other people or animals in or outside of the home, wear a mask.
    <br>
    Tell your close contacts that they may have been exposed to COVID-19. An infected person can spread COVID-19 starting 48 hours (or 2 days) before the person has any symptoms or tests positive. By letting your close contacts know they may have been exposed to COVID-19, you are helping to protect everyone.
</p>
<ul>
<li><p>Additional guidance is available for those living in close quarters and shared housing.</li></p>

<li><p>See COVID-19 and Animals if you have questions about pets.</li></p>
<li><p>If you are diagnosed with COVID-19, someone from the health department may call you. Answer the call to slow the spread.</li></p>
    </ul>
<h4>Monitor your symptoms</h4>
<ul>
<li><p>Symptoms of COVID-19 include fever, cough, or other symptoms.</li></p>
<li><p>Follow care instructions from your healthcare provider and local health department. Your local health authorities may give instructions on checking your symptoms and reporting information.</li></p>
